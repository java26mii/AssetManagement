    public void showTable(String key) {
        DefaultTableModel model = (DefaultTableModel) tblAsset.getModel();
        model.setRowCount(0);
        Object[] row = new Object[12];
        List<Asset> asset = new ArrayList<>();
        if (key == "") {
            asset = aco.getAll();
        }
        asset = aco.search(key);
//        emp = eco.search(key);

        for (int i = 0; i < asset.size(); i++) {
            row[0] = i + 1;
            row[1] = asset.get(i).getId();
            row[2] = asset.get(i).getName();
            row[3] = asset.get(i).getQuantity();
            row[4] = asset.get(i).getCategory();
            row[5] = asset.get(i).getNote();

            model.addRow(row);
        }
    }
